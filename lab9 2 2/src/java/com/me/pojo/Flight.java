package com.me.pojo;

/**
 *
 * @author mansiagrawal
 */
public class Flight {
      private int id;
    private String title;

    public Flight(){}
    public Flight(String title) {
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
 @Override 
	public String toString(){
		return title;
	}   
}
